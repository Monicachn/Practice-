tabby_cat = "\tI'm tabbed in." #\t代表Tab，即该行缩进8个空格
persian_cat = "I'm split\non a line." #此处\n代表换行，python优先默认\后的n有含义
backslash_cat = "I'm \\ a \\ cat." #此处输出结果为I'm \ a \ cat.

fat_cat= """
I'll do a list:
\t* Cat_food
\t* Fishies
\t* Catnip\n\t* Grass
""" #\t 代表Tab键，即该行缩进8个空格

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
